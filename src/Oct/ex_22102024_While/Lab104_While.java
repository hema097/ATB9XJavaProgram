package Oct.ex_22102024_While;

public class Lab104_While {
    public static void main(String[] args) {
        //Entry Control loop
        //we need to satisfy the condition then we will use while loop
        int i = 0;
        while (i >0) {
            System.out.println("Enter");
            System.out.println(i);
            i++;
        }
    }
}
