package Oct.ex_22102024_While;

public class Lab102 {
    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {

            System.out.println(i);
            i++;
        }
        System.out.println("Enter the for loop:While and for loop both are same:");
        for (int j=0;j<10;j++)
            System.out.println(j);
    }
}

